package com.eliorcohen.apptest.ModelsPackage;

public class RVModel {

    private String textRV, imageRV;

    public RVModel(String textRV, String imageRV) {
        this.textRV = textRV;
        this.imageRV = imageRV;
    }

    public String getTextRV() {
        return textRV;
    }

    public void setTextRV(String textRV) {
        this.textRV = textRV;
    }

    public String getImageRV() {
        return imageRV;
    }

    public void setImageRV(String imageRV) {
        this.imageRV = imageRV;
    }

}
